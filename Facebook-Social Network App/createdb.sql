CREATE TABLE "ZIPCODE_REF" 
(	
	"ZIPCODE" NUMBER(5,0) NOT NULL ENABLE, 
	"CITY" VARCHAR2(50 BYTE) NOT NULL ENABLE, 
	"STATE" VARCHAR2(20 BYTE) NOT NULL ENABLE, 
	"COUNTRY" VARCHAR2(20 BYTE) NOT NULL ENABLE, 
	CONSTRAINT "ZIPCODE_REF_PK" PRIMARY KEY ("ZIPCODE")
);

CREATE TABLE "ADDRESS" 
(
	"AID" NUMBER(5,0) NOT NULL ENABLE,
	"STREET_NO" NUMBER(10,0) NOT NULL ENABLE,
	"STREET_ADDRESS" VARCHAR2(200 BYTE) NOT NULL ENABLE,
	"ZIPCODE" NUMBER(5,0) NOT NULL ENABLE,
	"COORDINATES" SDO_GEOMETRY,
	CONSTRAINT "ADDRESS_PK" PRIMARY KEY ("AID"),
	CONSTRAINT "ZIPCODE_FK" FOREIGN KEY ("ZIPCODE")
		REFERENCES "ZIPCODE_REF" ("ZIPCODE") ON DELETE CASCADE ENABLE
);

CREATE TABLE "EVENT" 
(
	"TITLE" VARCHAR2(30 BYTE) NOT NULL ENABLE, 
	"DESCRIPTION" VARCHAR2(250 CHAR) NOT NULL ENABLE, 
	"ADDRESS_ID" NUMBER(5,0) NOT NULL ENABLE, 
	"STARTING_TIME" DATE, 
	"DURATION" VARCHAR2(20 BYTE) NOT NULL ENABLE, 
	CONSTRAINT "EVENT_PK" PRIMARY KEY ("TITLE"),
	CONSTRAINT "ADDRESS_FKEY" FOREIGN KEY ("ADDRESS_ID")
		REFERENCES "ADDRESS" ("AID") ON DELETE CASCADE ENABLE
);

CREATE TABLE "MEMBER" 
(	
	"EMAIL" VARCHAR2(20 BYTE) NOT NULL ENABLE, 
	"PASSWORD" VARCHAR2(10 BYTE) NOT NULL ENABLE, 
	"FNAME" VARCHAR2(20 BYTE) NOT NULL ENABLE, 
	"LNAME" VARCHAR2(20 BYTE) NOT NULL ENABLE, 
	"BIRTHDATE" DATE NOT NULL ENABLE, 
	"AID" NUMBER(5,0) NOT NULL ENABLE, 
	CONSTRAINT "MEMBER_PK" PRIMARY KEY ("EMAIL"),
	CONSTRAINT "AID_FK" FOREIGN KEY ("AID")
		REFERENCES "ADDRESS" ("AID") ON DELETE CASCADE ENABLE
);

CREATE TABLE "FRIENDSHIP" 
(
	"USER1" VARCHAR2(50 CHAR) NOT NULL ENABLE, 
	"USER2" VARCHAR2(50 CHAR) NOT NULL ENABLE, 
	"TYPE" VARCHAR2(20 BYTE) NOT NULL ENABLE, 
	 CONSTRAINT "USER1_FKEY" FOREIGN KEY ("USER1")
	  REFERENCES "MEMBER" ("EMAIL") ON DELETE CASCADE ENABLE, 
	 CONSTRAINT "USER2_FKEY" FOREIGN KEY ("USER2")
	  REFERENCES "MEMBER" ("EMAIL") ON DELETE CASCADE ENABLE
);

CREATE TABLE "POST" 
(
	"PID" NUMBER(5,0) NOT NULL ENABLE, 
	"NOTE" VARCHAR2(500 BYTE) NOT NULL ENABLE, 
	"SENDER" VARCHAR2(50 BYTE) NOT NULL ENABLE, 
	"PRIVACY" NUMBER(1,0) NOT NULL ENABLE, 
	"DATETIME" DATE NOT NULL ENABLE, 
	CONSTRAINT "POST_PK" PRIMARY KEY ("PID"),
	CONSTRAINT "SENDER_FKEY" FOREIGN KEY ("SENDER")
		REFERENCES "MEMBER" ("EMAIL") ON DELETE CASCADE ENABLE
);

CREATE TABLE "POST_COMMENT" 
(
	"PID" NUMBER(5,0) NOT NULL ENABLE, 
